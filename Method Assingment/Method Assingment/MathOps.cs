using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Method_Assingment
{
    class MathOps
    {
        public int Sum(int f, int g)
        {
            return (f + g);
        }

        public int Subtract(int f, int g)
        {
            return (f - g);
        }

        public int Multuply(int f, int g)
        {
            return (f * g);
        }

    }
}
